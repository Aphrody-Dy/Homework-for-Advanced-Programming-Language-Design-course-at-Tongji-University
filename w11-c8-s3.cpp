/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：只给出了上下标尺，其它均需要自己补充
***************************************************************************/
int main()
{
	cout << "请输入要打印的行数[3..13]" << endl;
	int n, a[13][13] = {0};
	cin >> n;
	for (int i = 0;i < n;i++) {
		a[i][n-1] = 1;
		a[i][n-1-i] = 1;
	}
	for (int i = 1;i < n;i++) {
		for (int j = 0;j < n-1;j++) {
			a[i][j] = a[i - 1][j] + a[i - 1][j+1];
		}
	}
	/* 上标尺 */
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "0         1         2         3         4         5         6         7" << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;

	//杨辉三角
	for (int i = 0;i < n;i++) {
		for (int j = 0;j < n;j++) {
			if (a[i][j]) {
				cout << setw(6) << right << a[i][j];
			}
			else {
				cout << setw(6) << right << " ";
			}
		}
		cout << endl;
	}

	/* 下标尺 */
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "0         1         2         3         4         5         6         7" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << endl;

	return 0;
}

